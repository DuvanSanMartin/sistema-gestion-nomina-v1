    private void generarInformeDesdeArchivo() {
        StringBuilder informe = new StringBuilder();
    String archivoCSV = "nominaproce.csv";

    try (BufferedReader br = new BufferedReader(new FileReader(archivoCSV))) {
        String linea;
        while ((linea = br.readLine()) != null) {
            String[] campos = linea.split(",");
            String nombre = campos[0];
            String apellido = campos[1];
            String edad = campos[2];
            String categoria = campos[3];
            double salarioBruto = Double.parseDouble(campos[4]);
            double deducciones = Double.parseDouble(campos[5]);
            double iess = Double.parseDouble(campos[6]);
            double salarioTotal = Double.parseDouble(campos[7]);

            informe.append("Nombre: ").append(nombre).append("\n");
            informe.append("Apellido: ").append(apellido).append("\n");
            informe.append("Edad: ").append(edad).append("\n");
            informe.append("Categor√≠a: ").append(categoria).append("\n");
            informe.append("Salario Bruto: ").append(salarioBruto).append("\n");
            informe.append("Deducciones: ").append(deducciones).append("\n");
            informe.append("IESS: ").append(iess).append("\n");
            informe.append("Salario Total: ").append(salarioTotal).append("\n\n");
        }

        JTextArea textArea = new JTextArea(informe.toString());
        textArea.setEditable(false);
        JScrollPane scrollPane = new JScrollPane(textArea);
        
        JOptionPane.showMessageDialog(this, scrollPane, "Informe de N√≥mina", JOptionPane.INFORMATION_MESSAGE);
    } catch (IOException | NumberFormatException ex) {

    }
    }
